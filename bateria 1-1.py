# Programa que pregunta dos números a l'usuari i calcula la seva suma.
numero1 = input("Si us plau entreu el primer número necessari per la suma: ")
numero2 = input("Si us plau entreu el segon número necessari per la suma: ")
resultat = float(numero1) + float(numero2)
print(numero1, "+", numero2, "=", resultat)
