# Programa que pregunta un número a l'usuari i diu si és major o menor que 100.
num = float(input("Insereu un nùmero si us plau: "))
if num > 100:
  print("El número que has triat és superior a 100.")
elif num == 100:
  print("El número que has triat és igual a 100.")
else:
  print("El número que has triat és inferior a 100.")
